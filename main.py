from microbit import *
import music
import neopixel

# นำเข้าคลาส SuperBit ที่เราสร้างไว้
from superbit import SuperBit   # สมมติว่าไฟล์ชื่อ superbit.py

# สร้างออบเจ็กต์
sb = SuperBit()

# -------------------------------
# 1. ควบคุมมอเตอร์

# -------------------------------
# หมุนมอเตอร์ M1 ไปข้างหน้า
sb.motor_run(8, 200)

# หมุนมอเตอร์ M2 ย้อนกลับ
sb.motor_run(10, -150)

# หมุนมอเตอร์ M3 ย้อนกลับ
sb.motor_run(12, -150)

# หมุนมอเตอร์ M4 ย้อนกลับ
sb.motor_run(14, -150)

# ควบคุมสองมอเตอร์พร้อมกัน (เช่น รถวิ่งตรง)
sb.motor_run_dual(8, 200, 12, 200)

sleep(2000)
sb.stop_motor(8)   # หยุดมอเตอร์
sb.stop_motor(12)   # หยุดมอเตอร์

# -------------------------------
# 2. ควบคุม Servo
# -------------------------------
sb.servo(1, 90)   # เซอร์โว 180° หมุนไปที่ 90°
sb.servo2(2, 135) # เซอร์โว 270° หมุนไปที่ 135°
sb.servo3(3, "forward", 45)  # เซอร์โว 360° หมุนไปข้างหน้า

# -------------------------------
# 3. ควบคุม RGB LED
# -------------------------------
strip = sb.rgb_program()
strip[0] = (255, 0, 0)   # LED0 = แดง
strip[1] = (0, 255, 0)   # LED1 = เขียว
strip[2] = (0, 0, 255)   # LED2 = น้ำเงิน
strip.show()

# -------------------------------
# 4. เล่นเพลง
# -------------------------------
sb.play_music("birthday")

